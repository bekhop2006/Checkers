/**
 * Copies NEXT_PUBLIC_* vars from root .env to frontend/.env.local
 * so Next.js dev server always sees Supabase keys.
 */
import { readFileSync, writeFileSync, existsSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const envPath = join(root, ".env");
const outPath = join(root, "frontend", ".env.local");

if (!existsSync(envPath)) {
  console.warn("sync-public-env: no root .env found");
  process.exit(0);
}

const lines = readFileSync(envPath, "utf8").split("\n");
const publicLines = lines.filter((line) => {
  const t = line.trim();
  return t.startsWith("NEXT_PUBLIC_") && t.includes("=");
});

if (publicLines.length === 0) {
  console.warn("sync-public-env: no NEXT_PUBLIC_* in .env");
  process.exit(0);
}

const header = `# Generated by scripts/sync-public-env.mjs — do not edit manually\n`;
writeFileSync(outPath, header + publicLines.join("\n") + "\n");
console.log(`sync-public-env: wrote ${publicLines.length} vars to frontend/.env.local`);
